                        <pre>                                 Kali River-Dandeli                                      River Rafting</pre><br>
                        <p> Located on the Western Ghats of Uttara Karnataka,<b><i>Dandeli</i></b> is bestowed with the lush green forests and lies on the banks of the Kali river. Known for its many fantastic adventure opportunities, Dandeli is a hub for river rafting on river kali . Adventure seekers have many options such as mountain biking, trekking, cycling, kayaking and canoeing. The Dandeli Wildlife Sanctuary is another exciting element that Dandeli has to offer.  This is perfect for nature lovers too as they can spot Bisons and Black Panthers here as well as be amidst beautiful flora as well. Dandeli has famous attractions here like the Kavla Caves, Ulavi Temple, Satholi Falls and the famous Sykes Point which gives tourists a breathtaking view of Dandeli. Camping is a popular activity here and there are plenty of operators here that provide fantastic services and memorable experiences to the guests.
