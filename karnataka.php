                        <br><p><b><i>Hampi</i></b>, the city of ruins, is a UNESCO World Heritage Site. Situated in the shadowed depth of hills and valleys in the state of Karnataka, this place is a historical delight for travellers. Surrounded by 500 ancient monuments, monolithic sculptures, beautiful temples, bustling street markets, bastions, treasury building and captivating remains of Vijayanagar Empire, which are a major part of its tourism, Hampi is a backpacker's delight and attract travelers because of their excellent workmanship. The Hindu style of architecture found at Hampi reflects the splendor of the Vijayanagara Empire. The rugged landscape adds to the historic ambiance of this site.Hampi is an open museum with 100+ locations to explore and a favourite way to see the city from the perspective of its history.</p>
