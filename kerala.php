                        <pre>                                    Edakkal Caves                                                                              
                        </pre>
                        <br><p>The <b><i>Edakkal Caves</i></b>  are two natural caves at a remote location at Edakkal, 25 km (15.5 mi) from Kalpetta in the Wayanad district of Kerala in India. They lie 1,200 m above sea level on Ambukutty Mala, near an ancient trade route connecting the high mountains of Mysore to the ports of the Malabar Coast. Inside the caves are pictorial writings believed to date to at least 6,000 BCE, from the Neolithic man, indicating the presence of a prehistoric settlement in this region.The Stone Age carvings of Edakkal are rare and are the only known examples from South India besides those of Shenthurini, Kollam, also in Kerala.The cave paintings of Shenthurini forests in Kerala are of the Mesolithic era (middle stone-age).Edakkal Caves' story is shrouded in mystery. As per the legends, the caves were formed because of Luv and Kush – sons of Lord Rama. It’s said that their arrows split a large rock into two, thus forming this narrow passage.
                        </p>
