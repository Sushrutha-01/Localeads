                        <pre>                                    Belum Caves                                                                              
                        </pre>
                        <br><p>The <b><i>Belum Caves</i></b> The caves in Belum are the longest in India after the ones in the state of Meghalaya, known for their cave formations, such as stalactite and stalagmite formations. The Belum Caves have long passages, galleries, spacious caverns with fresh water, and siphons.This cave system was formed over the course of tens of thousands of years by the constant flow of underground water from the now-disappeared river Chitravathi. The cave system reaches its deepest point (151 ft from entrance level) at the point known as Pataalaganga. It is one of the centrally protected Monuments of National Importance.Belum Caves derive their name from "Bilum" Sanskrit word for caves.In Telugu language, it is called Belum Guhalu.Belum Caves are geologically and historically important caves. There are indications that Jains and Buddhists monks occupied these caves centuries ago. Many Buddhists relics were found inside the caves. These relics are now housed in Museum at Ananthapur. Archaeological survey of India (ASI) also found remnants of vessels of the pre-Buddhist era and dated the remnants of these objects to 4500 years BCE.
                        </p>
