                        <pre>                                Lepakshi Temple</pre><br>
                        <p><b><i>Lepakshi Temple</i></b> Lepakshi is a small quaint village with a trail of several great kingdoms that once ruled the area. Located in the Anantpur district of Andhra Pradesh.Founded in 1535 AD by Maharaja Aliya Rama Raya of the Vijayanagara empire, Lepakshi is a trove of several fascinating archaeological sites, beautiful ancient temples and rich culture.. Locals believe that according to the legend of Ramayana, Jatayu fell in Lepakshi after getting injured by Ravana. The village is most famous as being home to the Lepakshi Temple - known for the stunning Vijaynagar style of architecture and hanging pillars.. Apart from richly carved temples with inscriptions in Kannada and hanging pillar, the most striking feature of Lepakshi is the largest monolithic idol of Nandi Bull in the entire subcontinent made of granite.Lepakshi is a treasure trove of a fascinating range of art forms and traditional crafts.items like Banjara embroidery, Brassart ware, Cotton and jute durries, Kalamkari paintings with their colorful depictions of the epics and landscapes, Kondapalli toys carved out of softwood, Cherial scroll paintings and the Bidricraft with silvery contours are among the best things to shop for.

