                        <pre>                                     Araku Valley</pre>
                        <br><p>The <b><i>Araku Valley</i></b>  is a comparatively unexplored hill station mostly visited by locals as a weekend getaway and is situated some 120 kilometres away from Vishakhapatnam.Located in the gentle hills of Eastern Ghats, Araku Valley is also home to a number of tribes as well. This valley is also home to a few tribal caves and the Museum of Tribal Arts, which are an experience in their own right. This destination is quite famous for its exquisite coffee, whose subtle aroma will have you craving for a cuppa. In addition to being a beautiful vista point, you can also try your hand at a number of adventure sports here, which include trekking and swimming as well., Araku is famous for its indigenous tribal culture.The Tribal Museum showcases the daily life of these tribes, their culture and food habits.The main gallery in the museum is two storeyed and has dioramas of dances, ritualistic rooms and hunting scenes amongst others. Also on display are household articles, musical instruments and local art.
