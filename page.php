                        <br><p>The <b><i>Horsely Hills</i></b>is a picturesque hamlet in the state of Andhra Pradesh. Situated at an altitude of 4,100 ft this serene hill station is named after W.D. Horsley, the collector of Cuddapah district. It is located at a distance of 144 Km from Tirupati and is an ideal summer resort for spending a fascinating vacation. The scenic drive to this mesmerizing hamlet is extremely panoramic and scenic and has thick vegetation of trees lining on both sides of the road. The other major draw for the travelers is the relaxing climate of the Horsley Hills which ranges from 20°C to 32°C even in summers. The hill is lush with dense forest and thick greenness and has a wide variety of trees like Gulmohar, Allamanda, Reeta, Amla, Beedi leaves, Blue Gum, Eucalyptus and Sandalwood.
