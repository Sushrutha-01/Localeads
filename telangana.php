                        <pre>                               Stepwell-Bansilalpet</pre><br>
                        <p>Built in the 17th Century during the Kakatiya Dynasty, the <b><i>Bansilalpet stepwell </i></b> was in use until the 1970's and went into a state of ruin due to negligence. Thanks to the efforts of the local government, the stepwell as cleared off of nearly 2200 tonnes of waste, restoring it to its former glory. In mid-2022, the revival and restoration of the stepwell began with the Telangana Urban Development Department as a partner. Tonnes of garbage were removed — revealing the six-stage stepwell descending 50 feet into the ground. The restored stepwell was finally opened to the public on December 5, 2022. More than 2000 tons of debris was removed during the restoration process. The stepwell is a sight to behold in its restored glory. The light fixtures at several parts of the walkways, the beautiful water underneath and calming Carnatic music playing in the background made for the perfect setting for a peaceful evening here.</p>
